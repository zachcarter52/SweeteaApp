@Composable
fun MainScreen() {
    val navController = rememberNavController()

    val navItemList = listOf(

        NavItem("Home", Icons.Filled.Home),
        NavItem("Menu", Icons.Filled.Menu),
        NavItem("Rewards", Icons.Filled.Star),
        NavItem("Account", Icons.Filled.AccountCircle)
    )

    var selectedIndex by remember {
        mutableIntStateOf(0)
    }

    Scaffold(
        modifier = Modifier.fillMaxSize(),
        bottomBar = {
            NavigationBar{
                navItemList.forEachIndexed{ index, navItem ->
                    NavigationBarItem(
                        selected = selectedIndex == index,
                        onClick = {
                            selectedIndex = index
                        },
                        icon = {
                            Icon(imageVector = navItem.icon, contentDescription = "Icon")
                        },
                        label = {
                            Text(text = navItem.label)
                        }
                    )
                }
            }
        }
    ) { innerPadding ->
        MainScreenNavigationConfiguration(modifier = Modifier.padding(innerPadding),selectedIndex)
    }
}

@Composable
fun MainScreenNavigationConfiguration(modifier: Modifier = Modifier, selectedIndex : Int) {
    when(selectedIndex){
        0 -> HomePage()
        1 -> MenuPage()
        2 -> RewardsPage()
        3 -> AccountPage()

    }
}


